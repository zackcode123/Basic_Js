/*4 - Faça um algoritmo que receba (prompt) um valor de um produto e mostre um novo valor deste produto atualizado, para o
 novo valor deverá ser concedido o desconto de 25%.  */

 valor1 = Number(prompt("Digite o valor original: "))
 valorDesconto = (valor1*25)/100

 alert(`O valor original é ${valor1} e com desconto de 25% fica ${valorDesconto}`)